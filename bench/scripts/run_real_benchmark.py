RunnerFn = Callable[[McpSession, str, str, int], str]


class TokenCounter:
    def __init__(self, encoding_name: str | None) -> None:
        self.encoding_name = encoding_name
        self.mode = "heuristic_char4"
        self._encoder = None
        if encoding_name:
            try:
                import tiktoken  # type: ignore

                self._encoder = tiktoken.get_encoding(encoding_name)
                self.mode = f"tiktoken:{encoding_name}"
            except Exception:
                self.mode = "heuristic_char4"

    def count(self, text: str) -> int:
        if not text:
            return 0
        if self._encoder:
            return len(self._encoder.encode(text))
        return int(math.ceil(len(text) / 4.0))


def percentile(values: list[float], p: float) -> float:
    if not values:
        return 0.0
    if len(values) == 1:
        return float(values[0])
    ordered = sorted(values)
    idx = (len(ordered) - 1) * p
    low = int(math.floor(idx))
    high = int(math.ceil(idx))
    if low == high:
        return float(ordered[low])
    low_v = ordered[low]
    high_v = ordered[high]
    return float(low_v + (high_v - low_v) * (idx - low))


def calc_reduction(baseline_value: float, wrapper_value: float) -> float | None:
    if baseline_value <= 0:
        return None
    return ((baseline_value - wrapper_value) / baseline_value) * 100.0


def clip(text: str, max_len: int) -> str:
    if len(text) <= max_len:
        return text
    return text[: max_len - 3] + "..."


def aggregate_runs(runs: list[dict[str, Any]]) -> dict[str, Any]:
    total = len(runs)
    success_count = sum(1 for run in runs if run["success"])
    latencies = [float(run["latency_ms"]) for run in runs]
    input_tokens = sum(int(run["input_tokens"]) for run in runs)
    output_tokens = sum(int(run["output_tokens"]) for run in runs)
    return {
        "runs": total,
        "success_count": success_count,
        "success_rate": (success_count / total) if total else 0.0,
        "input_tokens": input_tokens,
        "output_tokens": output_tokens,
        "total_tokens": input_tokens + output_tokens,
        "latency_ms": {
            "p50": percentile(latencies, 0.50),
            "p95": percentile(latencies, 0.95),
            "max": max(latencies) if latencies else 0.0,
            "mean": (sum(latencies) / len(latencies)) if latencies else 0.0,
        },
        "failures": [run for run in runs if not run["success"]],
    }


def bootstrap_ratio_reduction_ci(
    pairs: list[tuple[int, int]],
    *,
    samples: int,
    confidence: float,
    seed: int,
) -> dict[str, float] | None:
    if not pairs:
        return None
    n = len(pairs)
    rng = random.Random(seed)
    reductions: list[float] = []
    alpha = (1.0 - confidence) / 2.0

    for _ in range(samples):
        base_sum = 0
        wrap_sum = 0
        for _ in range(n):
            base_tok, wrap_tok = pairs[rng.randrange(n)]
            base_sum += base_tok
            wrap_sum += wrap_tok
        if base_sum <= 0:
            continue
        reductions.append(((base_sum - wrap_sum) / base_sum) * 100.0)

    if not reductions:
        return None

    reductions.sort()
    low = percentile(reductions, alpha)
    high = percentile(reductions, 1.0 - alpha)
    return {"low": low, "high": high, "confidence": confidence}


def make_request_texts(unity_instance: str) -> dict[str, dict[str, str]]:
    """Return normalized request texts for both modes.

    Baseline: tool_name + arguments JSON (what the LLM actually generates).
    JSON-RPC envelope is NOT included as it is added by the MCP client
    framework, not generated by the LLM.

    Wrapper: CLI command string (what the LLM generates).
    """
    return {
        "hierarchy_query": {
            "baseline": (
                'manage_scene({"action":"get_hierarchy","page_size":120,"cursor":0,'
                f'"max_depth":4,"include_transform":true,"unity_instance":"{unity_instance}"}})'
            ),
            "wrapper": (
                f"uni-cli hierarchy ls --instance {unity_instance} "
                "--fields id,name,parent --limit 120 --cursor 0"
            ),
        },
        "object_create": {
            "baseline": (
                'manage_gameobject({"action":"create","name":"BenchObj","primitive_type":"Cube",'
                f'"position":[0,1,0],"unity_instance":"{unity_instance}"}})'
            ),
            "wrapper": (
                f"uni-cli object create --instance {unity_instance} "
                "--name BenchObj --preset cube --pos 0,1,0"
            ),
        },
        "reparent": {
            "baseline": (
                'manage_gameobject({"action":"modify","target":"BenchChild","search_method":"by_name",'
                f'"parent":"BenchParent","unity_instance":"{unity_instance}"}})'
            ),
            "wrapper": (
                f"uni-cli object reparent --instance {unity_instance} "
                "--id BenchChild --parent BenchParent"
            ),
        },
        "asset_search": {
            "baseline": (
                'manage_asset({"action":"search","path":"Assets","filter_type":"Script",'
                f'"page_size":80,"page_number":1,"generate_preview":false,"unity_instance":"{unity_instance}"}})'
            ),
            "wrapper": (
                f'uni-cli asset search --instance {unity_instance} --query "t:Script" '
                "--fields path,name --limit 80 --cursor 0"
            ),
        },
        "batch_ops": {
            "baseline": (
                'batch_execute({"commands":[{"tool":"manage_gameobject","params":{"action":"create"}},'
                '{"tool":"manage_gameobject","params":{"action":"modify"}}],'
                f'"unity_instance":"{unity_instance}"}})'
            ),
            "wrapper": (
                f"uni-cli batch apply --instance {unity_instance} "
                "--file batch_ops.json --fields status,id --limit 3 --cursor 0"
            ),
        },
    }


SCENARIOS: list[tuple[str, RunnerFn]] = [
    ("hierarchy_query", run_hierarchy),
    ("object_create", run_create),
    ("reparent", run_reparent),
    ("asset_search", run_asset_search),
    ("batch_ops", run_batch),
]


def execute_scenario(
    *,
    sid: str,
    runner: RunnerFn,
    session: McpSession,
    resolved_instance: str,
    request_texts: dict[str, dict[str, str]],
    token_counter: TokenCounter,
    iterations: int,
    warmup: int,
    max_preview: int,
) -> dict[str, Any]:
    runs_by_mode: dict[str, list[dict[str, Any]]] = {"baseline": [], "wrapper": []}

    for mode in ("baseline", "wrapper"):
        for iteration in range(warmup + iterations):
            request_text = request_texts[sid][mode]
            start = time.perf_counter()
            success = True
            returncode = 0
            stderr = ""
            try:
                stdout = runner(session, resolved_instance, mode, iteration)
            except Exception as exc:  # pragma: no cover - runtime integration path
                stdout = ""
                stderr = f"err code=SCENARIO_FAILED msg={exc}"
                returncode = 1
                success = False
            latency_ms = (time.perf_counter() - start) * 1000.0

            record = {
                "scenario_id": sid,
                "mode": mode,
                "iteration": iteration,
                "returncode": returncode,
                "latency_ms": latency_ms,
                "success": success,
                "input_tokens": token_counter.count(request_text),
                "output_tokens": token_counter.count((stdout or "") + (stderr or "")),
                "total_tokens": token_counter.count(request_text)
                + token_counter.count((stdout or "") + (stderr or "")),
                "stdout_preview": clip(stdout, max_preview),
                "stderr_preview": clip(stderr, max_preview),
            }

            if iteration >= warmup:
                runs_by_mode[mode].append(record)

    baseline_agg = aggregate_runs(runs_by_mode["baseline"])
    wrapper_agg = aggregate_runs(runs_by_mode["wrapper"])

    # Output-only reduction (primary metric — response payload comparison)
    output_reduction = calc_reduction(
        baseline_agg["output_tokens"], wrapper_agg["output_tokens"]
    )
    # Total (input+output) reduction (secondary metric)
    total_reduction = calc_reduction(
        baseline_agg["total_tokens"], wrapper_agg["total_tokens"]
    )

    # Detect degenerate statistics (zero intra-scenario variance)
    base_output_set = set(int(x["output_tokens"]) for x in runs_by_mode["baseline"])
    wrap_output_set = set(int(x["output_tokens"]) for x in runs_by_mode["wrapper"])
    is_degenerate = len(base_output_set) <= 1 and len(wrap_output_set) <= 1

    # Only compute CI when there is actual variance
    ci95 = None
    ci_note = None
    if is_degenerate:
        ci_note = (
            "Degenerate: all iterations produced identical token counts. "
            "CI requires variance from different scene configurations."
        )
    else:
        pairs = list(
            zip(
                [int(x["output_tokens"]) for x in runs_by_mode["baseline"]],
                [int(x["output_tokens"]) for x in runs_by_mode["wrapper"]],
            )
        )
        ci95 = bootstrap_ratio_reduction_ci(
            pairs, samples=2000, confidence=0.95, seed=42
        )

    return {
        "id": sid,
        "iterations": iterations,
        "baseline": baseline_agg,
        "wrapper": wrapper_agg,
        "comparison": {
            "output_reduction_pct": output_reduction,
            "total_reduction_pct": total_reduction,
            "output_ci95_bootstrap": ci95,
            "ci_note": ci_note,
            "success_rate_delta_pct_point": (
                wrapper_agg["success_rate"] - baseline_agg["success_rate"]
            )
            * 100.0,
            "latency_p95_delta_pct": calc_reduction(
                baseline_agg["latency_ms"]["p95"], wrapper_agg["latency_ms"]["p95"]
            ),
        },
        "runs": runs_by_mode,
    }


def compute_overall(scenarios: list[dict[str, Any]]) -> dict[str, Any]:
    per_scenario_out = []
    for scenario in scenarios:
        out_red = scenario["comparison"]["output_reduction_pct"]
        if out_red is not None:
            per_scenario_out.append(out_red)

    total_base_out = sum(s["baseline"]["output_tokens"] for s in scenarios)
    total_wrap_out = sum(s["wrapper"]["output_tokens"] for s in scenarios)
    total_base = sum(s["baseline"]["total_tokens"] for s in scenarios)
    total_wrap = sum(s["wrapper"]["total_tokens"] for s in scenarios)

    return {
        "output_reduction_pct_per_scenario": per_scenario_out,
        "output_reduction_median_pct": percentile(sorted(per_scenario_out), 0.5)
        if per_scenario_out
        else None,
        "output_reduction_range_pct": {
            "min": min(per_scenario_out) if per_scenario_out else None,
            "max": max(per_scenario_out) if per_scenario_out else None,
        },
        "output_reduction_weighted_pct": calc_reduction(total_base_out, total_wrap_out),
        "total_reduction_weighted_pct": calc_reduction(total_base, total_wrap),
        "baseline_total_tokens": total_base,
        "wrapper_total_tokens": total_wrap,
        "baseline_output_tokens": total_base_out,
        "wrapper_output_tokens": total_wrap_out,
        "baseline_success_rate": (
            sum(s["baseline"]["success_count"] for s in scenarios)
            / sum(s["baseline"]["runs"] for s in scenarios)
        ),
        "wrapper_success_rate": (
            sum(s["wrapper"]["success_count"] for s in scenarios)
            / sum(s["wrapper"]["runs"] for s in scenarios)
        ),
    }


def build_cross_scene_comparison(scene_reports: list[dict[str, Any]]) -> dict[str, Any]:
    scenario_rows: dict[str, list[dict[str, Any]]] = {sid: [] for sid, _ in SCENARIOS}

    for scene in scene_reports:
        profile = scene["profile"]
        for scenario in scene["scenarios"]:
            scenario_rows[scenario["id"]].append(
                {
                    "scene_profile": profile,
                    "output_reduction_pct": scenario["comparison"][
                        "output_reduction_pct"
                    ],
                    "total_reduction_pct": scenario["comparison"][
                        "total_reduction_pct"
                    ],
                    "baseline_output_tokens": scenario["baseline"]["output_tokens"],
                    "wrapper_output_tokens": scenario["wrapper"]["output_tokens"],
                }
            )

    return {
        "scenario_output_reduction_pct": scenario_rows,
    }


def summarize_run(run_report: dict[str, Any]) -> str:
    """Per-scenario summary with output-only and total reduction metrics."""
    lines: list[str] = []
    lines.append(
        "Scenario                         BaseOut  WrapOut  OutRed%  "
        "BaseTot  WrapTot  TotRed%  BaseSucc  WrapSucc"
    )
    lines.append("-" * 105)

    per_scenario_output_reductions: list[float] = []
    per_scenario_total_reductions: list[float] = []

    for scenario in run_report["scenarios"]:
        sid = scenario["id"][:30]
        base = scenario["baseline"]
        wrap = scenario["wrapper"]
        comp = scenario["comparison"]
        out_red = comp["output_reduction_pct"]
        tot_red = comp["total_reduction_pct"]
        out_txt = f"{out_red:7.2f}" if out_red is not None else "   n/a "
        tot_txt = f"{tot_red:7.2f}" if tot_red is not None else "   n/a "
        ci_note = comp.get("ci_note", "")
        note_mark = " *" if ci_note else ""
        lines.append(
            f"{sid:<30} {base['output_tokens']:>8} {wrap['output_tokens']:>8} {out_txt:>8} "
            f"{base['total_tokens']:>8} {wrap['total_tokens']:>8} {tot_txt:>8} "
            f"{base['success_rate'] * 100:>8.1f}% {wrap['success_rate'] * 100:>8.1f}%{note_mark}"
        )
        if out_red is not None:
            per_scenario_output_reductions.append(out_red)
        if tot_red is not None:
            per_scenario_total_reductions.append(tot_red)

    lines.append("-" * 105)

    # Per-scenario summary stats (median, min, max)
    if per_scenario_output_reductions:
        sorted_out = sorted(per_scenario_output_reductions)
        median_out = percentile(sorted_out, 0.5)
        lines.append(
            f"Output reduction per-scenario: "
            f"median={median_out:.1f}% "
            f"range=[{sorted_out[0]:.1f}%..{sorted_out[-1]:.1f}%]"
        )
    if per_scenario_total_reductions:
        sorted_tot = sorted(per_scenario_total_reductions)
        median_tot = percentile(sorted_tot, 0.5)
        lines.append(
            f"Total reduction per-scenario:  "
            f"median={median_tot:.1f}% "
            f"range=[{sorted_tot[0]:.1f}%..{sorted_tot[-1]:.1f}%]"
        )

    # Note about degenerate CIs
    any_degenerate = any(
        s["comparison"].get("ci_note") for s in run_report["scenarios"]
    )
    if any_degenerate:
        lines.append(
            "* = single-configuration (zero intra-scenario variance, CI not meaningful)"
        )

    return "\n".join(lines)


def summarize_scene_run(run_report: dict[str, Any]) -> str:
    lines: list[str] = []
    for scene in run_report["scenes"]:
        lines.append(f"[scene={scene['profile']}] {scene['scene_path']}")
        lines.append(summarize_run({"scenarios": scene["scenarios"]}))
        overall = scene["overall"]
        lines.append(
            f"  output reduction: median={overall['output_reduction_median_pct']:.1f}% "
            f"weighted={overall['output_reduction_weighted_pct']:.1f}%"
        )
        lines.append("")

    lines.append("Cross-scene output reduction by scenario")
    lines.append("Scenario                         small    medium    large")
    lines.append("-" * 64)
    rows = run_report.get("cross_scene", {}).get("scenario_output_reduction_pct", {})
    for sid, _ in SCENARIOS:
        values = {
            item["scene_profile"]: item["output_reduction_pct"]
            for item in rows.get(sid, [])
        }
        s_small = values.get("small")
        s_medium = values.get("medium")
        s_large = values.get("large")
        t_small = f"{s_small:7.2f}" if s_small is not None else "   n/a "
        t_medium = f"{s_medium:7.2f}" if s_medium is not None else "   n/a "
        t_large = f"{s_large:7.2f}" if s_large is not None else "   n/a "
        lines.append(f"{sid[:30]:<30} {t_small:>8} {t_medium:>9} {t_large:>8}")

    return "\n".join(lines)


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--url", default="http://127.0.0.1:8080/mcp")
    parser.add_argument("--unity-instance", default="uni-cli")
    parser.add_argument("--iterations", type=int, default=30)
    parser.add_argument("--warmup", type=int, default=3)
    parser.add_argument("--repetitions", type=int, default=3)
    parser.add_argument("--timeout", type=float, default=30.0)
    parser.add_argument("--encoding", default="o200k_base")
    parser.add_argument("--out", default="reports/real-trust-latest.json")
    parser.add_argument("--max-preview", type=int, default=220)
    parser.add_argument("--strict", action="store_true")
    parser.add_argument(
        "--scene-profile",
        choices=["small", "medium", "large", "all"],
        default=None,
        help="Switch benchmark scene before running: small, medium, large, or all",
    )
    args = parser.parse_args()

    if args.iterations <= 0:
        raise SystemExit("--iterations must be > 0")
    if args.warmup < 0:
        raise SystemExit("--warmup must be >= 0")
    if args.repetitions <= 0:
        raise SystemExit("--repetitions must be > 0")

    token_counter = TokenCounter(args.encoding)
    request_texts = make_request_texts(args.unity_instance)
    scene_profiles = resolve_scene_profiles(args.scene_profile)

    report: dict[str, Any] = {
        "name": "real-unity-benchmark-trust",
        "generated_at_utc": datetime.now(timezone.utc).isoformat(),
        "url": args.url,
        "unity_instance_selector": args.unity_instance,
        "token_counter_mode": token_counter.mode,
        "iterations": args.iterations,
        "warmup": args.warmup,
        "repetitions": args.repetitions,
        "scene_profile": args.scene_profile,
        "runs": [],
        "cross_run": {},
    }

    any_failure = False

    for rep in range(1, args.repetitions + 1):
        session = McpSession(url=args.url, timeout_sec=args.timeout)
        session.initialize()
        resolved_instance = resolve_instance_id(session, args.unity_instance)

        run_report: dict[str, Any] = {
            "run_index": rep,
            "resolved_instance": resolved_instance,
            "scene_profile": args.scene_profile,
        }

        if scene_profiles:
            run_report["scenes"] = run_with_scene_profiles(
                session,
                resolved_instance,
                scene_profiles,
                lambda profile, scene_meta: {
                    "profile": profile,
                    "scene_name": scene_meta["scene_name"],
                    "scene_path": scene_meta["scene_path"],
                    "scenarios": [
                        execute_scenario(
                            sid=sid,
                            runner=runner,
                            session=session,
                            resolved_instance=resolved_instance,
                            request_texts=request_texts,
                            token_counter=token_counter,
                            iterations=args.iterations,
                            warmup=args.warmup,
                            max_preview=args.max_preview,
                        )
                        for sid, runner in SCENARIOS
                    ],
                },
            )
            for scene in run_report["scenes"]:
                scene["overall"] = compute_overall(scene["scenarios"])
                for scenario_result in scene["scenarios"]:
                    if scenario_result["baseline"]["success_rate"] < 1.0:
                        any_failure = True
                    if scenario_result["wrapper"]["success_rate"] < 1.0:
                        any_failure = True
            run_report["cross_scene"] = build_cross_scene_comparison(
                run_report["scenes"]
            )

            merged_scenarios = [
                scenario
                for scene in run_report["scenes"]
                for scenario in scene["scenarios"]
            ]
            run_report["overall"] = compute_overall(merged_scenarios)
        else:
            run_report["scenarios"] = []
            for sid, runner in SCENARIOS:
                scenario_result = execute_scenario(
                    sid=sid,
                    runner=runner,
                    session=session,
                    resolved_instance=resolved_instance,
                    request_texts=request_texts,
                    token_counter=token_counter,
                    iterations=args.iterations,
                    warmup=args.warmup,
                    max_preview=args.max_preview,
                )
                run_report["scenarios"].append(scenario_result)
                if scenario_result["baseline"]["success_rate"] < 1.0:
                    any_failure = True
                if scenario_result["wrapper"]["success_rate"] < 1.0:
                    any_failure = True

            run_report["overall"] = compute_overall(run_report["scenarios"])

        report["runs"].append(run_report)

    # Cross-run summary
    all_medians = [
        float(x["overall"]["output_reduction_median_pct"])
        for x in report["runs"]
        if x["overall"]["output_reduction_median_pct"] is not None
    ]
    all_weighted_out = [
        float(x["overall"]["output_reduction_weighted_pct"])
        for x in report["runs"]
        if x["overall"]["output_reduction_weighted_pct"] is not None
    ]
    report["cross_run"] = {
        "output_reduction_median_per_run": all_medians,
        "output_reduction_weighted_per_run": all_weighted_out,
        "median_of_medians": percentile(sorted(all_medians), 0.5)
        if all_medians
        else None,
        "stdev_medians": (
            math.sqrt(
                sum(
                    (x - (sum(all_medians) / len(all_medians))) ** 2
                    for x in all_medians
                )
                / len(all_medians)
            )
            if all_medians
            else 0.0
        ),
    }

    if scene_profiles:
        combined: dict[str, dict[str, list[float]]] = {
            sid: {profile: [] for profile in scene_profiles} for sid, _ in SCENARIOS
        }
        for run in report["runs"]:
            rows = run.get("cross_scene", {}).get("scenario_output_reduction_pct", {})
            for sid, items in rows.items():
                for item in items:
                    profile = item["scene_profile"]
                    value = item["output_reduction_pct"]
                    if value is not None:
                        combined[sid][profile].append(float(value))

        report["cross_scene"] = {
            "scenario_output_reduction_pct": {
                sid: {
                    profile: {
                        "values": values,
                        "median": percentile(sorted(values), 0.5) if values else None,
                    }
                    for profile, values in profiles.items()
                }
                for sid, profiles in combined.items()
            }
        }

    out_path = Path(args.out)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    with out_path.open("w", encoding="utf-8") as fp:
        json.dump(report, fp, ensure_ascii=False, indent=2)

    for run in report["runs"]:
        print(f"\n[run {run['run_index']}]")
        if scene_profiles:
            print(summarize_scene_run(run))
        else:
            print(summarize_run(run))
        overall = run["overall"]
        print(
            f"  output reduction: median={overall['output_reduction_median_pct']:.1f}% "
            f"weighted={overall['output_reduction_weighted_pct']:.1f}%"
        )

    cross = report["cross_run"]
    cross_medians: list[float] = []
    raw_medians = cross.get("output_reduction_median_per_run")
    if isinstance(raw_medians, list):
        for value in raw_medians:
            if isinstance(value, (int, float)):
                cross_medians.append(float(value))

    median_of_medians = cross.get("median_of_medians")
    median_of_medians_txt = (
        f"{float(median_of_medians):.1f}%"
        if isinstance(median_of_medians, (int, float))
        else "n/a"
    )
    stdev_value = cross.get("stdev_medians")
    stdev_txt = (
        f"{float(stdev_value):.4f}" if isinstance(stdev_value, (int, float)) else "n/a"
    )
    print("\n[cross-run]")
    print(
        "output_reduction_medians="
        + ", ".join(f"{float(x):.1f}" for x in cross_medians)
        + f" | median_of_medians={median_of_medians_txt}"
        + f" stdev={stdev_txt}"
    )
    print(f"report: {out_path}")
    print(f"token_counter: {token_counter.mode}")

    if args.strict and any_failure:
        return 2
    return 0


